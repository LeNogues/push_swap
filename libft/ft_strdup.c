#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		size;
	char	*dup;
	int		i;

	i = 0;
	size = ft_strlen(s);
	dup = malloc(sizeof (char) * size + 1);
	if (!dup)
		return (0);
	while (s[i])
	{
		dup[i] = s[i];
		i++;
	}
	dup[i] = 0;
	return (dup);
}
